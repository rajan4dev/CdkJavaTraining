class Loops{

	public static void main(String[] s){

		for(int k=100;k<110;k++){
			System.out.println("K :"+k);
		}
		
		int j = 10;		
		do{
			System.out.println("J :"+j);
			j++;
		}while(j<=15);


		
		int i = 1;
		
		while(i <= 10){
			System.out.println("Missile launched to Pakistan.... count "+i);
			i = i + 1;
		}

		System.out.println("Pakistan Finish!!!!!");

	}	

}